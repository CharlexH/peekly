  function normalizeLocale(locale) {
    const value = String(locale || "").toLowerCase();
    if (value.startsWith("zh")) return "zh";
    if (value.startsWith("en")) return "en";
    return defaultLocale;
  }

  function interpolate(template, params) {
    if (!params) return template;
    return template.replace(/\{(\w+)\}/g, (_, key) => {
      return params[key] == null ? "" : String(params[key]);
    });
  }

  function translate(locale, key, params) {
    const normalized = normalizeLocale(locale);
    const template = messages[normalized]?.[key] || messages.en[key] || messages[defaultLocale][key] || key;
    return interpolate(template, params);
  }

  function translateText(locale, value) {
    if (value == null || value === "") return value;
    const normalized = normalizeLocale(locale);
    const text = String(value);
    if (normalized === "en") return text;
    if (textMap[normalized]?.[text]) return textMap[normalized][text];
    for (const rule of dynamicTextRules[normalized] || []) {
      const match = text.match(rule.re);
      if (match) return rule.fn(match);
    }
    return text;
  }

  function storageGet(key) {
    try {
      return global.localStorage?.getItem(key) || "";
    } catch (_) {
      return "";
    }
  }

  function storageSet(key, value) {
    try {
      global.localStorage?.setItem(key, value);
    } catch (_) {}
  }

  function initialLocale() {
    let requested = "";
    try {
      requested = new URLSearchParams(global.location?.search || "").get("lang") || "";
    } catch (_) {}
    const saved = storageGet("pk_locale");
    return normalizeLocale(requested || saved || defaultLocale);
  }

  function applyDocumentLocale(locale, titleKey) {
    const normalized = normalizeLocale(locale);
    const doc = global.document;
    if (!doc) return;
    if (doc.documentElement) {
      doc.documentElement.lang = normalized === "zh" ? "zh-CN" : "en";
      doc.documentElement.dataset.locale = normalized;
    }
    if (titleKey) {
      doc.title = translate(normalized, titleKey);
    }
  }

  function createState(titleKey) {
    return {
      locale: initialLocale(),
      t(key, params) {
        return translate(this.locale, key, params);
      },
      tx(value) {
        return translateText(this.locale, value);
      },
      isLocale(locale) {
        return this.locale === normalizeLocale(locale);
      },
      setLocale(locale) {
        this.locale = normalizeLocale(locale);
        storageSet("pk_locale", this.locale);
        applyDocumentLocale(this.locale, titleKey);
        if (typeof global.dispatchEvent === "function" && typeof global.CustomEvent === "function") {
          global.dispatchEvent(new CustomEvent("peekly:locale-change", { detail: { locale: this.locale } }));
        }
      },
      localeName(locale) {
        return translate(this.locale, `language.${normalizeLocale(locale)}`);
      },
      applyLocale() {
        applyDocumentLocale(this.locale, titleKey);
      },
    };
  }

